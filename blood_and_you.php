<h1 align="center">BLOOD AND YOU </h1>
<p style="font-size:20px"> We know that you would not donate unless you think your blood is safe. However, in order for us to assess all risks that may affect you or a patient receiving a transfusion, it is essential that you answer each question completely and accurately. If you don’t understand a question, ask the blood center staff. All information you provide is confidential.</p>

<h2>Knowing what to expect</h2>

<p style="font-size:15px"> Most people have uneventful donations and feel good about donating afterward, but some donors may have a lightheaded or dizzy feeling; an upset stomach; a black and blue mark, redness, or pain from the needle; fainting or loss of consciousness and injury from related falls; or very rarely, nerve or artery damage. Blood donation removes iron from the body and may cause or aggravate iron-deficiency anemia.</p>

<h2>To determine if you are eligible to donate, we will:</h2>
<p style="font-size:20px">   -->  Ask about your health and travel.<br> 
  -->  Ask about medicines you are taking or have taken.<br>
  -->  Ask about your risk for infections that can be transmitted by blood – especially AIDS and viral hepatitis.<br>
  -->  Take your blood pressure, temperature, and pulse.</p>

<h2>DO NOT DONATE IF YOU</h2>
<p style="font-size:20px">   -->  Have EVER used needles to take any drugs not prescribed by your doctor.<br> 
  -->  Have had syphilis or gonorrhea IN THE PAST 12 MONTHS.<br>
  -->  Have been in juvenile detention, lockup, jail, or prison for more than 72 consecutive hours IN THE LAST 12 MONTHS.<br>
  -->  Have EVER been diagnosed with the Ebola virus infection.</p>



