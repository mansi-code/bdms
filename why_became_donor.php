<h1><br><br>Why Become A Blood Donor??</h1>

<h2>Why Donate Blood?</h2>
<p style="font-size:20px"> A blood donation truly is a “gift of life” that a healthy individual can give to others in their community who are sick or injured. In one hour’s time, a person can donate one unit of blood that can be separated into four individual components that could help save multiple lives.</p>


<h2>What are the minimum requirements to become a blood donor?</h2>
<p style="font-size:20px"> Check with your local blood bank to determine the specific requirements. You must be at least 16 years of age, a minimum of 110 pounds, and in basic good health.</p>

<h2>Will donating blood hurt?</h2> 
<p style="font-size:20px"> You might feel a slight sting in the beginning lasting only a couple of seconds, but there should be no discomfort during the donation.</p>

